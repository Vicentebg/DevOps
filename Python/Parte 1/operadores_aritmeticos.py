# Adição
print("Adição")
print(1 + 1)

# Subtração
print("Subtração")
print(10 - 2)

# Multiplicação
print("Multiplicação")
print(4 * 3)

# Divisão
print("Divisão")
print(12 / 3)

# Divisão inteira
print("Divisão inteira")
print(12 // 3)

# Módulo
print("Módulo")
print(12 // 3)

# Exponenciação
print("Exponenciação")
print(2 ** 3)

# Ordem de precedência ou ordem das operações.
# Primeiro    | Segundo  | Terceiro      | Quarto  | Quinto | Sexto
# Parenteses > Expoentes > Multiplicação > Divisão > Soma > Subtração
# Sempre respeita a ordem da esquerda para direita
# () > ** > * > / > + > -