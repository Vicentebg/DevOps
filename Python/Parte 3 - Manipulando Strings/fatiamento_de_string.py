# Fatiamento de Strings

nome = "Ziraldo Alves Pinto"

print(nome[0]) # Pega o índice 0
print(nome[-1]) # Pega a última letra da String
print(nome[:9]) #Pega os 9 primeiros caracteres
print(nome[10:]) # Os últimos 10 caracteres daa direita para esquerda
print(nome[10:16]) # Sugere um intervalo de 6 caracteres
print(nome[10:16:2]) # Sugere um intervalo indo de dois em dois
print(nome[:]) # Printa a string completa
print(nome[::-1]) # Espelha a string

