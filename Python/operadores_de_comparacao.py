# Igualdade
print("Igualdade")

saldo = 450
saque = 200

print (saldo == saque)

# False quando a condição não é atendida e True quando é atendida

# Diferença
print("Diferença")

saldo = 450
saque = 200

print (saldo != saque)

# Maior que/ menor ou igual
print("Maior, menor ou igual")

saldo = 450
saque = 200

print (saldo > saque) # True

print (saldo < saque) # False

print (saldo >= saque) # True

print (saldo <= saque) # False